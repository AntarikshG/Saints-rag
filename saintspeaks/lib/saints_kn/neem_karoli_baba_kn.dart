ಶರಣಾಗತಿ ಅಕ್ರಿಯೆಯಲ್ಲ; ಇಚ್ಛೆಯನ್ನು ದಿವ್ಯಇಚ್ಛೆಗೆ ಹೊಂದಿಸಿಕೊಳ್ಳುವುದು. ವ್ಯಕ್ತಿಗತ ಆಸೆ-ಭಯಗಳನ್ನು ಬಿಡಿಸಿದಾಗ ದಿವ್ಯವು ನಮ ಮೂಲಕ ಕಾರ್ಯನಿರ್ವಹಿಸುತ್ತದೆ.

