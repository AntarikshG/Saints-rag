      body: '''ನೀಂ ಕರೋಲಿ ಬಾಬಾ — ಎಲ್ಲ ಧರ್ಮಗಳೂ ನಿಷ್ಠೆಯಿಂದ ಅನುಸರಿಸಿದರೆ ಅದೇ ಸತ್ಯಕ್ಕೆ ಕರೆದೊಯ್ಯುತ್ತವೆ ಎಂದರು. "ಎಲ್ಲ ಧರ್ಮಗಳೂ ಒಂದೇ; ಎಲ್ಲರೂ ಒಂದೇ" — ಆಶ್ರಮದ ಬಾಗಿಲು ಎಲ್ಲರಿಗೂ ತೆರೆದಿತ್ತು.

ಕ್ರೈಸ್ತ, ಮುಸ್ಲಿಂ, ಬೌದ್ಧ, ಯೆಹೂದಿ, ಹಿಂದೂ — ಧರ್ಮಪರಿಚಯ ಬೇಡ; ದಿವ್ಯಪ್ರೀತಿ ಸಾಕು. ಬಾಬಾ ಕ್ರಿಸ್ತನನ್ನೂ ಮಹಾಪ್ರೇಮದಿಂದ ಸ್ಮರಿಸುತ್ತಿದ್ದರು — ರಾಮ ಮತ್ತು ಕ್ರಿಸ್ತ ಒಂದೇ ದಿವ್ಯ ಚೈತನ್ಯದ ರೂಪಗಳು ಎಂದು ಹೇಳುತ್ತಿದ್ದರು.

